def translate(name):
    parts = name.split()
    if len(parts) >= 2:
        prefix, suffix = parts[0], parts[-1]
        t_suffix_data = suffixes.get(suffix)
        if not t_suffix_data:
            return name
        t_suffix, gender = t_suffix_data
        
        # Determine prefix based on gender
        # Default to M if unknown
        g_code = gender[0] if gender else "M"
        if g_code not in ["M", "F", "N"]:
            g_code = "M"
            
        t_prefix_dict = prefixes.get(prefix)
        if not t_prefix_dict:
            return f"{prefix} {t_suffix}"
            
        t_prefix = t_prefix_dict.get(g_code, t_prefix_dict["M"])
        
        return f"{t_prefix} {t_suffix}"
    return name

