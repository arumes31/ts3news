plural_nouns = {"Останки", "Обломки", "Глубины", "Архивы", "Грибы", "Ноги", "Товары", "Копы", "Очки"}

def translate(en_name):
    parts = en_name.split(" ")
    if len(parts) != 2:
        return en_name # Fallback
    
    adj = parts[0]
    noun = parts[1]
    
    ru_adj = adj_map.get(adj, adj)
    ru_noun = noun_map.get(noun, noun)
    
    if ru_noun in plural_nouns:
        # Plural
        if ru_adj.endswith('ий'): ru_adj = ru_adj[:-2] + 'ые'
        elif ru_adj.endswith('ый'): ru_adj = ru_adj[:-2] + 'ые'
        elif ru_adj.endswith('ой'): ru_adj = ru_adj[:-2] + 'ые'
    elif ru_noun.endswith(('а', 'я')):
        # Feminine
        if ru_adj.endswith('ий'): ru_adj = ru_adj[:-2] + 'ая'
        elif ru_adj.endswith('ый'): ru_adj = ru_adj[:-2] + 'ая'
        elif ru_adj.endswith('ой'): ru_adj = ru_adj[:-2] + 'ая'
    elif ru_noun.endswith(('о', 'е')):
        # Neuter
        if ru_adj.endswith('ий'): ru_adj = ru_adj[:-2] + 'ое'
        elif ru_adj.endswith('ый'): ru_adj = ru_adj[:-2] + 'ое'
        elif ru_adj.endswith('ой'): ru_adj = ru_adj[:-2] + 'ое'
    
    return f"{ru_adj} {ru_noun}"

